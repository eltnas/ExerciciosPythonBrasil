num = int(input("Qual seu dia da semana (1-Domingo, 2-Segunda, 3-Terça, 4-Quarta, 5-Quinta, 6-Sexta, 7-Sabado:"))

if num == 1:
	print("Seu dia é Domingo")
elif num == 2:
	print("Seu dia é Segunda-Feira!")
elif num == 3:
	print("Seu dia é Terça-Feira!")
elif num == 4:
	print("Seu dia é Quarta-Feira!")
elif num == 5:
	print("Seu dia é Quinta-Feira!")
elif num == 6:
	print("Seu dia é Sexta-Feira!")
elif num == 7:
	print("Seu dia é Sabado!")
else:
	print("Opção inválida!")